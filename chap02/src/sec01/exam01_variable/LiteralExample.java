package sec01.exam01_variable;

public class LiteralExample {
	public static void main(String[] args) {
		int var1 = 10;
		
		int var2 = 010;
		
		int var3 = 0x10;
		
		double var4 = 0.25;
		
		double var5 = 2E5;
		
		char var6 = 'A';
		
		char var7 = '한';
		
		System.out.println("대한" + '\n' + "민국");
		
		char var8 = '\u0041';
		
		System.out.println(var8);
		
		boolean var10 = true;
		
		boolean var11 = false;
		
	}
}
